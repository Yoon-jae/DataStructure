
public class Entry {
	
	private Object _key, _value;
	
	public Entry(Object key, Object value) {
		this._key = key;
		this._value = value;
	}

	public Object key() {
		return this._key;
	}

	public Object value() {
		return this._value;
	}

	public void setKey(Object key) {
		this._key = _key;
	}

	public void setValue(Object value) {
		this._value = _value;
	}
	
	
	
}
